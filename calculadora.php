  $valor1 = 10;
  $valor2 = 5;
  $resultado = $valor1 * $valor2;
  print_r($resultado);
}

function divide() {
  $valor1 = 10;
  $valor2 = 5;
  $resultado = $valor1 / $valor2;
  print_r($resultado);
}

sum();
